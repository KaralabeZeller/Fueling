void SzovKi(const struct StruTxt *txt)
{
uint16_t		i, ch, x, y;

x= txt->x; y= txt->y;
for(i=0; i<32; i++){
	ch= txt->str[EE.Nyelv][i];
	if(ch == 0) break;
	G_Kar1_Ki(x, y, ch);
	x+= C1_SZEL;
	}	
}
