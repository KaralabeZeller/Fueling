//-----------------------------------------------Karakter1(8*12pixel)
void G_Kar1_Ki(uint16_t x, uint16_t ybe, uint8_t ch)
{
uint16_t		y, sor, minta;
const uint8_t	*chtbl;

chtbl= C1Cimek[ch];
for(sor=0; sor<C1_COL; sor++){
	minta= chtbl[sor];							//bitminta
	y= ybe+sor;
	PutPixel(x  , y, minta&0x80);
	PutPixel(x+1, y, minta&0x40);
	PutPixel(x+2, y, minta&0x20);
	PutPixel(x+3, y, minta&0x10);
	PutPixel(x+4, y, minta&0x08);
	PutPixel(x+5, y, minta&0x04);
	PutPixel(x+6, y, minta&0x02);
	PutPixel(x+7, y, minta&0x01);
	}	
}
//-----------------------------------------------Karakter2(16*24pixel)
void G_Kar2_Ki(uint16_t xbe, uint16_t y, uint16_t wide, uint8_t ch)
{
uint16_t		x, sor;
char			szin;
uint16_t		minta;
const uint16_t	*chtbl;

chtbl= C2Cimek[ch];
for(sor=0; sor<C2_COL; sor++){
	minta= chtbl[sor];							//bitminta
	for(x=xbe; x<(xbe+wide); x++){				//oszlop
		if(minta&0x8000) szin= 1; else szin= 0;
		PutPixel(x, y, szin);
		minta<<= 1;
		}			
	y++;
	}	
}
//-----------------------------------------------Karakter3(24*36pixel)
void G_Kar3_Ki(uint16_t xbe, uint16_t y, uint16_t wide, uint8_t ch)
{
uint16_t	x, sor;
char		szin;
uint32_t	minta;

if(ch == ' ') return;							//0 kioltásnál gyorsít
if(wide > 22) wide= 22;
for(sor=0; sor<36; sor++){
	minta= Chr3_tolt(ch,sor);					//bitminta
	for(x=xbe; x<(xbe+wide); x++){				//oszlop
		if(minta&0x200000) szin= 1; else szin= 0;
		PutPixel(x, y, szin);
		minta<<= 1;
		}			
	y++;
	}	
}
