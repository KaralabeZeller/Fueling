void	OutStr1(uint16 db, uint16 x, uint16 y)
{
uint16	i;

if(db > 32) db= 32;
for(i=0; i<db; i++){
	if(Str[i] == 0) break;
	G_Kar1_Ki(x, y, Str[i]);
	x+= C1_SZEL;
	}	
}
//-----------------------------------------------
void OutStr2(uint16_t db, uint16_t x, uint16_t y)
{
uint16_t		i, wide;

if(db > 32) db= 32;
for(i=0; i<db; i++){
	if(Str[i] == 0) break;								//stop
	else if((Str[i]=='.')||(Str[i]==':'))wide= C2_SZEL/2+1;	//keskeny
	else								 wide= C2_SZEL+1;	//normal
	G_Kar2_Ki(x, y, wide, Str[i]);
	x+= wide;
	}
}
//-----------------------------------------------
void OutStr3(uint16_t db, uint16_t x, uint16_t y)
{
uint16_t		i, wide;

if(db > 9) db= 9;
for(i=0; i<db; i++){
	if(Str[i] == 0) break;				//stop
	else if(Str[i]=='.')	wide= 6;	//keskeny
	else					wide= 24;	//normal
	G_Kar3_Ki(x, y, wide, Str[i]);
	x+= wide;
	}
}
