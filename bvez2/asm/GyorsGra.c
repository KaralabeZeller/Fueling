uint32_t Chr3_tolt(uint8_t ch, uint8_t sor)
{
uint32_t	ki= 0;

if		((ch >= '0')&&(ch <= '9'))	ki= C3_[ch-'0'][sor];
else if	(ch == '.')					ki= C3_[10][sor];

return(ki);
}
